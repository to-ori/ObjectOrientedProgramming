package scanner;
//this class contains a Character and the appropriate getters, setters and constructors 
public class Glyph {

	
	private Character glyph;

	public Glyph(Character glyph){
		this.setGlyph(glyph);
	}

	public Character getGlyph() {
		return glyph;
	}

	public void setGlyph(Character glyph) {
		this.glyph = glyph;
	}
}


